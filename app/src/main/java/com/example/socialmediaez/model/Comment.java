package com.example.socialmediaez.model;

public class Comment {

    private int id, id_user, id_post;
    private String content;

    public int getId() {
        return id;
    }

    public int getId_user() {
        return id_user;
    }

    public int getId_post() {
        return id_post;
    }

    public String getContent() {
        return content;
    }
}
